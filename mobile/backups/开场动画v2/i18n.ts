const listeners = new Set<(language: SystemLanguage) => void>();

export function systemLanguageLabel(language: SystemLanguage) {
  return labels[language];
}

export function translate(language: SystemLanguage, key: TranslationKey, vars?: Record<string, string | number>) {
  let text = dictionaries[language][key] ?? english[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replace(`{${name}}`, String(value));
    }
  }
  return text;
}

export async function setAppSystemLanguage(language: SystemLanguage) {
  await updateSystemLanguage(language);
  listeners.forEach((listener) => listener(language));
}

export function useSystemLanguage() {
  const [language, setLanguage] = useState<SystemLanguage>("English");

  useEffect(() => {
    let mounted = true;
    getSystemLanguage().then((value) => {
      if (mounted) setLanguage(value);
    });
    const listener = (value: SystemLanguage) => setLanguage(value);
    listeners.add(listener);
    return () => {
      mounted = false;
      listeners.delete(listener);
    };
  }, []);

  const t = useCallback(
    (key: TranslationKey, vars?: Record<string, string | number>) => translate(language, key, vars),
    [language]
  );

  return { language, t, setLanguage: setAppSystemLanguage };
}
